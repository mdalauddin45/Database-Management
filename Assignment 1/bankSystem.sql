CREATE DATABASE bank;
USE BANK;
CREATE TABLE SIGNUP(FROM_NO VARCHAR(30),NAME VARCHAR(30), FATHER_NAME VARCHAR(30),DOB VARCHAR(30), GENDER VARCHAR(30), EMAIL VARCHAR(50), MARITAL_STATUS VARCHAR(30), ADDRESS VARCHAR(100), CITY VARCHAR(100), PINCODE VARCHAR(30), STATE VARCHAR(100));
SELECT*FROM SIGNUP;
CREATE TABLE SIGNUPTWO(FROM_NO VARCHAR(30),RELIGION VARCHAR(30), CATEGORY VARCHAR(30), INCOME VARCHAR(30), EDUCATION VARCHAR(30), OCCUPTION VARCHAR(50), NID VARCHAR(30), SENIORCITIZEN VARCHAR(100),EXISTING_ACCOUNT VARCHAR(100));
SELECT*FROM SIGNUPTWO;
CREATE TABLE SIGNUPTHREE(FROM_NO VARCHAR(30),ACCOUNT_TYPE VARCHAR(30), CARD_NUMBER VARCHAR(30), PIN VARCHAR(30), FACILITY VARCHAR(200));
SELECT*FROM SIGNUPTHREE;
CREATE TABLE LOGIN(FROM_NO VARCHAR(30), CARD_NUMBER VARCHAR(50),PIN VARCHAR(30));
SELECT*FROM LOGIN;
CREATE TABLE BANK(PIN VARCHAR(30), DATE VARCHAR(30), TYPE VARCHAR(30), AMOUNT VARCHAR(30));
SELECT*FROM BANK;